package com.demo;
// post/pre increment and decrement operators
public class prog7 {
    public static void main(String[] args){
        int num = 6;
        System.out.println(num++);
        System.out.println(num);
        System.out.println(++num);
        System.out.println(num);

        System.out.println(num--);
        System.out.println(num);
        System.out.println(--num);
        System.out.println(num);

    }
}